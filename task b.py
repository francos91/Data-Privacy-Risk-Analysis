QI_COLS = ["Country","rating_numeric","review_day_of_week","hour_bin","review_length_bin"]

def k_anon(f, c): return f.groupby(c, observed=True).size().min()
def uniq(f, c):
    gs = f.groupby(c, observed=True).size(); return (gs==1).sum()/len(f)
def avg_gs(f, c): return f.groupby(c, observed=True).size().mean()
def at_risk(f, c, k=5):
    gs = f.groupby(c, observed=True).size(); return gs[gs<k].sum()/len(f)

df_base = df.dropna(subset=QI_COLS).copy()
base = {"k":k_anon(df_base,QI_COLS), "uniq":uniq(df_base,QI_COLS),
        "avg_gs":avg_gs(df_base,QI_COLS), "at_risk":at_risk(df_base,QI_COLS), "n":len(df_base)}
print(f"      Baseline: k={base['k']}, Unique={base['uniq']:.1%}, Avg group={base['avg_gs']:.1f}")

