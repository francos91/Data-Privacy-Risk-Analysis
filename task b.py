print("\n[Technique 1] Generalisation ...")
REGION_MAP = {"US":"N.America","CA":"N.America","MX":"N.America","GB":"Europe",
    "IE":"Europe","DE":"Europe","FR":"Europe","IT":"Europe","ES":"Europe",
    "NL":"Europe","DK":"Europe","SE":"Europe","NO":"Europe","PL":"Europe",
    "BE":"Europe","AT":"Europe","CH":"Europe","PT":"Europe","IN":"Asia",
    "JP":"Asia","CN":"Asia","KR":"Asia","PH":"Asia","SG":"Asia","AU":"Oceania",
    "NZ":"Oceania","ZA":"Africa","NG":"Africa","KE":"Africa","BR":"S.America",
    "AR":"S.America","CO":"S.America"}

def generalise(frame, level):
    g = frame.copy()
    if level >= 1:
        g["hour_bin"] = g["hour_bin"].map({"Night(0-6)":"Night","Morning(7-12)":"Daytime",
            "Afternoon(13-18)":"Daytime","Evening(19-24)":"Night"}).fillna("Unknown")
        g["review_length_bin"] = g["review_length_bin"].map({"Very Short":"Short",
            "Short":"Short","Medium":"Medium","Long":"Long","Very Long":"Long",
            "Extremely Long":"Long"}).fillna("Unknown")
    if level >= 2:
        g["Country"] = g["Country"].map(REGION_MAP).fillna("Other")
        g["review_day_of_week"] = g["review_day_of_week"].apply(
            lambda x: "Weekend" if x in {"Saturday","Sunday"} else "Weekday")
    if level >= 3:
        g["rating_numeric"] = g["rating_numeric"].apply(
            lambda x: "Negative" if x<=2 else ("Neutral" if x==3 else "Positive"))
    return g

qi_str = [str(c) for c in QI_COLS]
