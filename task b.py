print("\n[Technique 3] Differential Privacy ...")
def add_lap(tv, sens, eps): return tv + np.random.laplace(0, sens/eps)
def test_dp(frame, eps, n=100):
    tr = frame["rating_numeric"].value_counts().sort_index()
    td = frame["review_day_of_week"].value_counts()
    re, de = [], []
    for _ in range(n):
        pe = [abs(max(0,round(add_lap(tr.get(r,0),1,eps)))-tr.get(r,0))/max(tr.get(r,0),1) for r in [1,2,3,4,5]]
        re.append(np.mean(pe)*100)
        dd = [abs(max(0,round(add_lap(td[d],1,eps)))-td[d])/td[d] for d in td.index]
        de.append(np.mean(dd)*100)
    return np.mean(re), np.mean(de)

epsilons = [0.1, 0.5, 1.0, 2.0, 5.0, 10.0]
dp_rows = []
for eps in epsilons:
    re, de = test_dp(df_base, eps)
    plabel = "Very Strong" if eps<=0.5 else ("Strong" if eps<=1 else ("Moderate" if eps<=5 else "Weak"))
    dp_rows.append({"epsilon":eps,"rating_err":re,"dow_err":de,"privacy":plabel})
    print(f"      eps={eps:<5} | Rating err: {re:>5.2f}% | DoW err: {de:>5.2f}% | {plabel}")
dp_df = pd.DataFrame(dp_rows)
dp_df.to_csv(RES_DIR/"differential_privacy_results.csv", index=False)

